#!/usr/bin/env node
// Generator: reads research JSON files → emits src/lib/integrations/registry/catalog.generated.ts

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");

// ─── Constants ─────────────────────────────────────────────────────────────

const DROP_SLUGS = new Set(["deltek-govwin"]);

const GOOGLE_AVAILABLE = new Set([
  "google-calendar",
  "google-drive",
  "google-meet",
  "gmail",
]);

const VALID_CATEGORIES = new Set([
  "video", "messaging", "email_calendar", "dev", "pm", "crm",
  "finance", "hr", "support", "storage", "docs_esign", "marketing",
  "analytics", "identity", "automation", "gov",
]);

const CATEGORY_ORDER = {
  video: 1, messaging: 2, email_calendar: 3, dev: 4, pm: 5,
  crm: 6, finance: 7, hr: 8, support: 9, storage: 10,
  docs_esign: 11, marketing: 12, analytics: 13, identity: 14,
  automation: 15, gov: 16,
};

// ─── Load research files (fixed order) ────────────────────────────────────

const RESEARCH_DIR = path.join(ROOT, "docs", "superpowers", "research");
const FILES = [
  "comms-files.json",
  "build-ops.json",
  "business-ops.json",
  "service-security-gov.json",
];

const raw = [];
for (const file of FILES) {
  const data = JSON.parse(readFileSync(path.join(RESEARCH_DIR, file), "utf8"));
  raw.push(...data);
}

// ─── Drop + Dedupe ─────────────────────────────────────────────────────────

const seen = new Set();
const deduped = [];
for (const entry of raw) {
  if (DROP_SLUGS.has(entry.slug)) continue;
  if (seen.has(entry.slug)) continue;
  seen.add(entry.slug);
  deduped.push(entry);
}

// ─── Build + Promote + Validate ────────────────────────────────────────────

const providers = [];
for (const entry of deduped) {
  // Promote Google family
  const isGoogleAvailable = GOOGLE_AVAILABLE.has(entry.slug);
  const status = isGoogleAvailable ? "available" : entry.status;
  const connect = isGoogleAvailable ? "google" : entry.connect;

  // Build output object — only these fields (drop bigTechNoSimpleIcon etc.)
  const provider = {
    slug: entry.slug,
    name: entry.name,
    description: entry.description,
    icon: entry.icon,
    category: entry.category,
    status,
    connect,
    authType: entry.authType,
    sector: entry.sector,
    docsUrl: entry.docsUrl,
  };

  // configFields (optional) — carried through verbatim for `connect:"config"`
  // providers (e.g. GitHub's PAT install form). The install API splits any field
  // flagged `secret:true` OUT of plaintext Integration.config and into the sealed
  // vault. Only emitted when present so entries without it stay byte-identical.
  if (Array.isArray(entry.configFields)) {
    provider.configFields = entry.configFields;
  }

  // Validate
  if (!provider.name || !provider.name.trim()) {
    throw new Error(`Provider ${provider.slug}: empty name`);
  }
  if (!provider.description || !provider.description.trim()) {
    throw new Error(`Provider ${provider.slug}: empty description`);
  }
  if (!VALID_CATEGORIES.has(provider.category)) {
    throw new Error(`Provider ${provider.slug}: invalid category "${provider.category}"`);
  }
  if (!["available", "coming_soon"].includes(provider.status)) {
    throw new Error(`Provider ${provider.slug}: invalid status "${provider.status}"`);
  }
  if (!["google", "config", "none"].includes(provider.connect)) {
    throw new Error(`Provider ${provider.slug}: invalid connect "${provider.connect}"`);
  }
  if (provider.connect !== "none" && provider.status !== "available") {
    throw new Error(`Provider ${provider.slug}: connect="${provider.connect}" but status="${provider.status}" — only available providers can be connectable`);
  }
  if (provider.configFields) {
    for (const f of provider.configFields) {
      if (!f.key || !f.label) {
        throw new Error(`Provider ${provider.slug}: configField missing key/label`);
      }
      if (!["text", "url", "secret"].includes(f.type)) {
        throw new Error(`Provider ${provider.slug}: configField "${f.key}" invalid type "${f.type}"`);
      }
    }
  }

  providers.push(provider);
}

// ─── Sort: by category order, then by name ─────────────────────────────────

providers.sort((a, b) => {
  const orderDiff = (CATEGORY_ORDER[a.category] ?? 99) - (CATEGORY_ORDER[b.category] ?? 99);
  if (orderDiff !== 0) return orderDiff;
  return a.name.localeCompare(b.name);
});

// ─── Emit TypeScript ────────────────────────────────────────────────────────

const lines = [];
lines.push(
  "// AUTO-GENERATED by scripts/generate-integration-registry.mjs from docs/superpowers/research/*.json",
  "// Do not edit by hand. Re-run: node scripts/generate-integration-registry.mjs",
  'import type { IntegrationProvider } from "../registry";',
  "",
  "export const CATALOG: IntegrationProvider[] = [",
);

for (const p of providers) {
  // Serialize each provider as a single-line object entry
  const obj = JSON.stringify(p);
  lines.push(`  ${obj},`);
}

lines.push("];", "");

const outPath = path.join(ROOT, "src", "lib", "integrations", "registry", "catalog.generated.ts");
writeFileSync(outPath, lines.join("\n"), "utf8");

// ─── Summary ────────────────────────────────────────────────────────────────

const byCat = {};
for (const p of providers) {
  byCat[p.category] = (byCat[p.category] ?? 0) + 1;
}

console.log(`\nGenerated ${providers.length} providers → ${path.relative(ROOT, outPath)}\n`);
console.log("Per-category breakdown:");
for (const cat of Object.keys(CATEGORY_ORDER).sort((a, b) => CATEGORY_ORDER[a] - CATEGORY_ORDER[b])) {
  if (byCat[cat]) {
    console.log(`  ${cat.padEnd(16)} ${byCat[cat]}`);
  }
}

const availableProviders = providers.filter((p) => p.status === "available");
console.log(`\nAvailable (connectable): ${availableProviders.length}`);
for (const p of availableProviders) {
  console.log(`  ${p.slug} (${p.connect})`);
}
