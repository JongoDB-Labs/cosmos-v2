#!/usr/bin/env node
// Generator: reads research JSON files → emits src/components/integrations/brand-icons/brand-icons.generated.ts
// Uses simple-icons (ESM, CC0) at BUILD TIME only. Never imported from src/.
// Falls back to simple-icons-legacy (v13) for brands removed from current v16 for trademark reasons.

import * as simpleIcons from "simple-icons";
import * as legacyIcons from "simple-icons-legacy";
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");

// ─── Build slug → {path, hex} maps from both simple-icons versions ────────────

const current = new Map();
for (const v of Object.values(simpleIcons)) {
  if (v && typeof v.slug === "string" && typeof v.path === "string") {
    current.set(v.slug, { path: v.path, hex: v.hex });
  }
}

const legacy = new Map();
for (const v of Object.values(legacyIcons)) {
  if (v && typeof v.slug === "string" && typeof v.path === "string") {
    legacy.set(v.slug, { path: v.path, hex: v.hex });
  }
}

// ─── Conservative alias map: catalog icon value → real simple-icons slug ─────
// Only add an alias if the brand exists in current or legacy under a different
// slug AND you have confirmed it is the same brand. Do NOT guess.
const ALIASES = {
  // GoToMeeting was rebranded to "GoTo" — same product suite, different slug
  goto: "gotomeeting",
};

// ─── Collect unique icon values from research files ──────────────────────────

const RESEARCH_FILES = [
  "docs/superpowers/research/comms-files.json",
  "docs/superpowers/research/build-ops.json",
  "docs/superpowers/research/business-ops.json",
  "docs/superpowers/research/service-security-gov.json",
];

const allIconValues = new Set();
for (const relPath of RESEARCH_FILES) {
  const fullPath = path.join(ROOT, relPath);
  const entries = JSON.parse(readFileSync(fullPath, "utf-8"));
  for (const entry of entries) {
    if (typeof entry.icon === "string" && entry.icon.length > 0) {
      allIconValues.add(entry.icon);
    }
  }
}

// ─── Match catalog icons against simple-icons ───────────────────────────────
// Resolution order: current[v] → current[ALIASES[v]] → legacy[v] → legacy[ALIASES[v]]
// Prefer current over legacy (newer, more maintained paths/colors).

const matched = {};
const unmatched = [];
let legacyMatchCount = 0;
let aliasMatchCount = 0;

for (const iconValue of allIconValues) {
  const aliasSlug = ALIASES[iconValue];

  if (current.has(iconValue)) {
    matched[iconValue] = current.get(iconValue);
  } else if (aliasSlug && current.has(aliasSlug)) {
    matched[iconValue] = current.get(aliasSlug);
    aliasMatchCount++;
  } else if (legacy.has(iconValue)) {
    matched[iconValue] = legacy.get(iconValue);
    legacyMatchCount++;
  } else if (aliasSlug && legacy.has(aliasSlug)) {
    matched[iconValue] = legacy.get(aliasSlug);
    legacyMatchCount++;
    aliasMatchCount++;
  } else {
    unmatched.push(iconValue);
  }
}

// ─── Emit generated file ─────────────────────────────────────────────────────

const outDir = path.join(ROOT, "src/components/integrations/brand-icons");
mkdirSync(outDir, { recursive: true });
const outPath = path.join(outDir, "brand-icons.generated.ts");

const entries = Object.entries(matched)
  .sort(([a], [b]) => a.localeCompare(b))
  .map(([slug, { path: svgPath, hex }]) => {
    const escapedPath = svgPath.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
    return `  "${slug}": { path: "${escapedPath}", hex: "${hex}" },`;
  })
  .join("\n");

const output = `// AUTO-GENERATED by scripts/generate-brand-icons.mjs — do not edit.
// Static subset of simple-icons (https://simpleicons.org, CC0) for catalog brand marks.
// Sources: simple-icons v16 (current) + simple-icons v13 (legacy fallback for trademark-removed brands).
export const BRAND_ICONS: Record<string, { path: string; hex: string }> = {
${entries}
};
`;

writeFileSync(outPath, output, "utf-8");

// ─── Report ──────────────────────────────────────────────────────────────────

const totalUnique = allIconValues.size;
const matchedCount = Object.keys(matched).length;
const unmatchedSorted = unmatched.sort();

console.log(`simple-icons (current v16)    : ${current.size} icons`);
console.log(`simple-icons (legacy v13)     : ${legacy.size} icons`);
console.log(`Catalog unique icon values    : ${totalUnique}`);
console.log(`Matched                       : ${matchedCount}`);
console.log(`  — from legacy (v13 fallback): ${legacyMatchCount}`);
console.log(`  — via alias map             : ${aliasMatchCount}`);
console.log(`Unmatched (monogram)          : ${unmatchedSorted.length}`);
console.log("");
console.log("Unmatched list (these will render as colored monograms):");
console.log(unmatchedSorted.join(", "));
console.log("");
console.log(`Wrote: ${outPath}`);
